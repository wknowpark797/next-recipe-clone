// ISR 방식 작업 - 주기설정
// ISR 방식 화면 확인 - 빌드 후 npm run start
export async function getStaticProps() {
	const { data } = await axios.get(`/filter.php?c=Seafood`);
	// console.log('Data fetching on Server: ', data);

	// props로 데이터를 넘길 때 data 안쪽의 값까지 뽑아낸 후 전달
	return {
		props: data,
		revalidate: 60 * 60 * 24,
	};
}
